
public class GLoops {

	public static void main(String[] args) {
		
		
			//prints every number 1-5;
		for(int x = 1; x <= 5; x++){
			System.out.print(x);
		}
		
		
		System.out.println();
		
		
			//prints every number 1-5
		int x = 1;
		while(x <= 5){
			System.out.print(x);
			x++;
		}
		
		
		System.out.println();
		
		
			//prints every number 1-5
		x = 1;
		do{
			System.out.print(x);
			x++;
		} while(x <= 5);
		
		
		
	}

}
